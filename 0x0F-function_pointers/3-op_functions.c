#include "3-calc.h"
/**
 * op_add - This function add to number
 * @a: The first number
 * @b: The second number
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - This function sub to number
 * @a: The first number
 * @b: The second number
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - This function multiplies to number
 * @a: The first number
 * @b: The second number
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - This function return the result of the division of to number
 * @a: The first number
 * @b: The second number
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - This function returns the remainder of the division of a by b
 * @a: The first number
 * @b: The second number
 * Return: remainder of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
